
#Tipos de dados
#lista é variável
lista_exemplo_1 = [1,2,3,4,5,6]
lista_exemplo_2 = ['Nome','Sala','Morango',4,5]
lista_exemplo_3 = [1,'Sala',True,False]

#Tuplas são constantes (imutavel)
tupla_exemplo_1 = (2,3,4,)
tupla_exemplo_2 = ('Joao', 'Silva',4)

#Dicionario é pra organizar os dados(chave,valor)
dicionario = {'Nome':'Joao', 'Idade':29}

print(lista_exemplo_1)
print(lista_exemplo_2)
print(lista_exemplo_3)

print(tupla_exemplo_1)
print(tupla_exemplo_2)

print(dicionario)
